    }

    ///<summary>өнгөний HSV-г авна</summary>
    public static void RgbHsv(Color c, out float h, out float s, out float v) {
        Color.RGBToHSV(c, out h, out s, out v);
    }

    ///<summary>өнгөний hsv-г авна [hsvIdx 0-h 1-s 2-v]</summary>
    public static float Hsv(Color c, int hsvIdx) {
        float[] hsv = new float[3];
        RgbHsv(c, out hsv[0], out hsv[1], out hsv[2]);
        return hsv[hsvIdx];
    }

    ///<summary>өнгөний hsva-г өөрчилнө [hsva нь 0-с бага бол өөрчлөхгүй, dhsv-г нэмнэ, isRep үнэн бол давтана]</summary>
    public static Color Hsva(Color c, float h, float s, float v, float a = -1, float dh = 0, float ds = 0, float dv = 0, bool isRep = false) {
        float hh, ss, vv;
        RgbHsv(c, out hh, out ss, out vv);
        Vector4 r = new Vector4((h < 0 ? hh : h) + dh, (s < 0 ? ss : s) + ds, (v < 0 ? vv : v) + dv, a < 0 ? c.a : a);
        r = new Vector4(
            isRep && !M.Apx(dh, 0) ? M.Rep(r.x, 1) : M.Clamp01(r.x),
            isRep && !M.Apx(ds, 0) ? M.Rep(r.y, 1) : M.Clamp01(r.y),
            isRep && !M.Apx(dv, 0) ? M.Rep(r.z, 1) : M.Clamp01(r.z),
            r.w
        );
        return A(HsvRgb(r.x, r.y, r.z), r.w);
    }

    ///<summary>өнгөний rgba-г өөрчилнө [rgba нь 0-с бага бол өөрчлөхгүй]</summary>
    public static Color Rgba(Color c, float r, float g, float b, float a = -1) {
        return new Color(r < 0 ? c.r : r, g < 0 ? c.g : g, b < 0 ? c.b : b, a < 0 ? c.a : a);
    }

